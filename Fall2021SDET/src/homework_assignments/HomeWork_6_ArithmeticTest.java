package homework_assignments;

public class HomeWork_6_ArithmeticTest {
    public static void main(String[] args) {

        int number1 = 98;
        int number2 = 5;
        int sum = number1 + number2;
        int difference = number1 - number2;
        int product = number1 * number2;
        int division = number1 / number2;
        int remainder = number1 % number2;
        System.out.println("The sum, difference, product, division and remainder of 98 and 5 are: "
                +  "\nSum: " + sum + "\nDifference: " + difference + "\nProduct: " + product +
                "\nDivision: " + division + "\nRemainder: " + remainder);
    }
}


